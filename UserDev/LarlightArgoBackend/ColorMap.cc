void ColorMap::get(unsigned char* out, float in)
{
  float xx = (in+1.0)*0.5;
  if(xx<0) xx =0;
  if(xx>=1) xx = 0.099999;
  float r = (fTable.size()) * xx ;
  int irow = (int)r;
  float dx = r-irow;
  vector<float>& row1 = fTable[irow];
  vector<float>& row2 = fTable[irow+1];
  
  *(out)   = (unsigned char)(row1[0] + dx*(row2[0]-row1[0]));
  *(out+1) = (unsigned char)(row1[1] + dx*(row2[1]-row1[1]));
  *(out+2) = (unsigned char)(row1[2] + dx*(row2[2]-row1[2]));
}

